create table TBL_T_AGENCY (AGENCY_ID bigint not null auto_increment comment 'レコードの主キー',
		UPDATE_DATE datetime(3) not null comment '',
		AGENCY_COMPANY_ID varchar(32) not null comment '',
		CREATE_DATE datetime(3) not null comment '',
		CREATE_USER_ID varchar(32) not null comment '',
		CREATE_USER_NAME varchar(100) not null comment '',
		UPDATE_USER_ID varchar(32) not null comment '',
		UPDATE_USER_NAME varchar(100) not null comment '',
		primary key (AGENCY_ID),
		unique (AGENCY_COMPANY_ID)) comment='代理店' ENGINE=InnoDB;
create table TBL_T_COMPANY_MANAGEMENT (COMPANY_MANAGEMENT_ID bigint not null auto_increment comment 'レコードの主キー',
		UPDATE_DATE datetime(3) not null comment '',
		COMPANY_ID varchar(32) not null comment '',
		COMPANY_NAME varchar(100) comment '',
		AGENCY_COMPANY_ID varchar(32) not null comment '',
		RECAIUS_LICENSE_ID bigint not null comment '',
		RECAIUS_MODEL_ID integer not null comment '',
		ENERGY_THRESHOLD integer not null comment '0～1000',
		SAVE_VOICE bit comment 'true:音声解析時に音声ファイルを保存する false:音声解析時に音声ファイルの保存をしない',
		PERMIT_IP_ADDRESS varchar(3000) comment 'TEXT型で定義する',
		VOICE_KEEP_DAYS integer comment '',
		LOGICAL_DELETE_LOG_KEEP_DAYS integer comment '',
		CREATE_DATE datetime(3) not null comment '',
		CREATE_USER_ID varchar(32) not null comment '',
		CREATE_USER_NAME varchar(100) not null comment '',
		UPDATE_USER_ID varchar(32) not null comment '',
		UPDATE_USER_NAME varchar(100) not null comment '',
		primary key (COMPANY_MANAGEMENT_ID),
		unique (COMPANY_ID)) comment='企業管理' ENGINE=InnoDB;
create table TBL_T_RECAIUS_LICENSE (RECAIUS_LICENSE_ID bigint not null auto_increment comment 'レコードの主キー',

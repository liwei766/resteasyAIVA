create table TBL_T_SPEECH_LOG (SPEECH_LOG_ID bigint not null auto_increment comment 'レコードの主キー',
		UPDATE_DATE datetime(3) not null comment '',
		COMPANY_ID varchar(32) not null comment '',
		USER_ID varchar(32) not null comment '音声解析していた担当者',
		USER_NAME varchar(100) not null comment '',
		SPEECH_LOG_NO bigint not null comment '企業ごとの一連番号 （登録時に最大値＋1を設定）',
		TYPE varchar(2) not null comment '0：マイク 1：ファイル　',
		FILE_NAME varchar(256) comment '',
		START_DATE datetime(3) not null comment '',
		END_DATE datetime(3) not null comment '開始時は開始日時と同じ値を設定',
		CREATE_DATE datetime(3) not null comment '',
		CREATE_USER_ID varchar(32) not null comment '',
		CREATE_USER_NAME varchar(100) not null comment '',
		UPDATE_USER_ID varchar(32) not null comment '',
		UPDATE_USER_NAME varchar(100) not null comment '',
		DELETE_DATE datetime(3) comment '',
		primary key (SPEECH_LOG_ID),
		unique (COMPANY_ID,
		SPEECH_LOG_NO)) comment='音声解析ログ' ENGINE=InnoDB;
create table TBL_T_SPEECH_LOG_DETAIL (SPEECH_LOG_DETAIL_ID bigint not null auto_increment comment 'レコードの主キー',
